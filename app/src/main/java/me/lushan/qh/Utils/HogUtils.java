package me.lushan.qh.Utils;

import android.util.Log;

public class HogUtils {

    /**
     * info level
     * @param msg
     */
    public static void i(String msg) {
        Log.i("QH-INFO",msg);
    }

    /**
     *  warn level
     * @param msg
     */
    public static void w(String msg) {
        Log.w("QH-WARN",msg);
    }


}
